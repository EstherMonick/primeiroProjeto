public class PrimeiraCalculadora {
    public static void main(String[] args) {
        // Informando programa para usuario
        System.out.println("calculadora"); //Ferramenta para retornar para o usuário

        // Criando as variaveis
        int valor1, valor2, resposta;
        valor1 = 5;
        valor2 = 2;

        // Processamento
        resposta = valor1 + valor2;

        // Saida
        System.out.println("o valor de " + valor1 + " + " + valor2 + " = " + resposta + "."); //Ferramenta para retornar para o usuário

    }
}
